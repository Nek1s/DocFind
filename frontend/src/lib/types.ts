/**
 * Статус документа в пайплайне загрузки и обработки на сервере.
 * - uploading — файл отправляется на сервер (есть прогресс в процентах);
 * - indexing  — сервер извлекает текст и индексирует (прогресс неопределённый);
 * - done      — документ проиндексирован и доступен для поиска;
 * - error     — файл отклонён (формат/размер) или обработка не удалась.
 */
export type FileStatus = 'uploading' | 'indexing' | 'done' | 'error'

/** Активный фильтр списка документов. */
export type UploadFilter = 'all' | 'pdf' | 'docx' | 'error'

/** Документ, загружаемый пользователем в базу знаний. */
export interface UploadFile {
  id: string
  /** Имя файла с расширением. */
  name: string
  /** Размер в байтах. */
  size: number
  /** Человекочитаемый размер, например «2.4 МБ». */
  sizeLabel: string
  /** Расширение в нижнем регистре: pdf, docx или иное (для невалидных). */
  ext: string
  /** Дата добавления, например «29.06.2026, 14:30». */
  date: string
  status: FileStatus
  /** Прогресс загрузки 0–100 (только для status === 'uploading'). */
  progress?: number
  /** Текст ошибки, если status === 'error'. */
  error?: string
}
